package com.github.pwcong.jpstart.mvp.bean

class JPGif(var rome: String, var hiragana: Int, var katakana: Int) {
    override fun toString(): String {
        return "JPGif{" +
                "rome='" + rome + '\'' +
                ", hiragana=" + hiragana +
                ", katakana=" + katakana +
                '}'
    }
}